////////////////////////////////////////////////////////////////////////////////////////////////////

use anyhow::Result as anyResult;
use clap::Parser;

////////////////////////////////////////////////////////////////////////////////////////////////////

use cli::{Cli, Command};

////////////////////////////////////////////////////////////////////////////////////////////////////

mod cli;
mod cmd;
mod core;
mod util;

////////////////////////////////////////////////////////////////////////////////////////////////////

fn main() -> anyResult<()> {
    let cli = Cli::parse();
    match cli.command {
        None => core::run(cli)?,
        Some(Command::Identity) => cmd::identity::run()?,
        Some(Command::Completion { shell }) => cmd::completion::run(shell)?,
    }
    Ok(())
}

